# Define add fucntion
def add(a, b):
    result = a + b
    print(str(a) + " + " + str(b) + " = " + str(result))

# Sub function
def sub(a, b):
    result = a - b
    print(str(a) + " - " + str(b) + " = " + str(result))

# Def multiplication function
def multiply(a, b):
    result = a * b
    print(str(a) + " * " + str(b) + " = " + str(result))